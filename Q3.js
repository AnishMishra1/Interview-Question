//convert n number of object into a array using javascript

// Sample objects
let obj1 = { id: 1, name: 'John' };
let obj2 = { id: 2, name: 'Alice' };
let obj3 = { id: 3, name: 'Bob' };

// Pushing objects into an array
let arrayOfObjects = [];
arrayOfObjects.push(obj1, obj2, obj3);

console.log(arrayOfObjects); // Output: [{ id: 1, name: 'John' }, { id: 2, name: 'Alice' }, { id: 3, name: 'Bob' }]
